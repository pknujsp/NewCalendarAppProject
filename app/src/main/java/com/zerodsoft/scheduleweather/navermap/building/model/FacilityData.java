package com.zerodsoft.scheduleweather.navermap.building.model;

public class FacilityData
{
    private String vacantRoomCount;
    private String stairsCount;
    private String elevatorCount;
    private String toiletCount;
    private String entranceCount;
    private String movingWorkCount;

    public FacilityData()
    {

    }

    public String getVacantRoomCount()
    {
        return vacantRoomCount;
    }

    public void setVacantRoomCount(String vacantRoomCount)
    {
        this.vacantRoomCount = vacantRoomCount;
    }

    public String getStairsCount()
    {
        return stairsCount;
    }

    public void setStairsCount(String stairsCount)
    {
        this.stairsCount = stairsCount;
    }

    public String getElevatorCount()
    {
        return elevatorCount;
    }

    public void setElevatorCount(String elevatorCount)
    {
        this.elevatorCount = elevatorCount;
    }

    public String getToiletCount()
    {
        return toiletCount;
    }

    public void setToiletCount(String toiletCount)
    {
        this.toiletCount = toiletCount;
    }

    public String getEntranceCount()
    {
        return entranceCount;
    }

    public void setEntranceCount(String entranceCount)
    {
        this.entranceCount = entranceCount;
    }

    public String getMovingWorkCount()
    {
        return movingWorkCount;
    }

    public void setMovingWorkCount(String movingWorkCount)
    {
        this.movingWorkCount = movingWorkCount;
    }
}
